	movl %eax, %cr3

	movl $0xc0000080, %ecx		/* enable long mode */
	rdmsr
	btsl $8, %eax
	wrmsr

	movl %cr0, %eax				/* enable paging */
	btsl $31, %eax
	movl %eax, %cr0

	ljmp $0x10, $_start64		/* %cs = 0x10 */

